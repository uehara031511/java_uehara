package course30;

public class Pokemon {
		private String name;
		private int hp;

		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}

		public int getHp() {
			return hp;
		}
		public void setHp(int hp) {
			this.hp = hp;
		}


		private int no;
		public int getNo() {
			return no;
		}
		public void setNo(int no) {
			this.no = no;
		}
}
